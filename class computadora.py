class computadora:
    def __init__(self, marca, modelo, procesador, ram):
        self.__marca = marca
        self.__modelo = modelo
        self.__procesador = procesador
        self.__ram = ram
#getters
    def get_id(self):
        return self.__id
    
    def get_marca(self):
        return self.__marca
    
    def get_modelo(self):
        return self.__modelo
    
    def get_procesador(self):
        return self.__procesador

    def get_ram(self):
        return self.__ram
    
    

   
    